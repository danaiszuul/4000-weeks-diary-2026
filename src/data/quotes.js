/**
 * Get quote for a specific date
 */
export function getQuoteForDate(date = new Date()) {
  const month = date.getMonth() + 1; // 0-indexed to 1-indexed
  const dayOfMonth = date.getDate();
  
  // Filter quotes for this month
  const monthQuotes = quotes.filter(q => q.month === month);
  
  // Cycle through quotes based on day of month
  const index = (dayOfMonth - 1) % monthQuotes.length;
  
  return monthQuotes[index] || quotes[0];
}
